package com.example.zeve;

public class SnsData {
    private int snsContentImg;
    private String snsNickname;
    private String snsDate;
    private String snsContentTxt;

    public int getSnsContentImg() {
        return snsContentImg;
    }

    public String getSnsNickName() {
        return snsNickname;
    }

    public String getSnsDate() {
        return snsDate;
    }

    public String getSnsContentTxt() {
        return snsContentTxt;
    }

    public void setSnsContentImg(int snsContentImg) {
        this.snsContentImg = snsContentImg;
    }

    public void setSnsNickname(String snsNickname) {
        this.snsNickname = snsNickname;
    }

    public void setSnsDate(String snsDate) {
        this.snsDate = snsDate;
    }

    public void setSnsContentTxt(String snsContentTxt) {
        this.snsContentTxt = snsContentTxt;
    }
}
