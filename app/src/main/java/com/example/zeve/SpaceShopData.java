package com.example.zeve;

public class SpaceShopData {

    private int shopItemImg;
    private int shopItemCoin;
    private String shopItemName;

    public int getShopItemImg() {
        return shopItemImg;
    }

    public int getShopItemCoin() {
        return shopItemCoin;
    }

    public String getShopItemName() {
        return shopItemName;
    }

    public void setShopItemImg(int shopItemImg) {
        this.shopItemImg = shopItemImg;
    }

    public void setShopItemCoin(int shopItemCoin) {
        this.shopItemCoin = shopItemCoin;
    }

    public void setShopItemName(String shopItemName) {
        this.shopItemName = shopItemName;
    }
}
